<?php

namespace May\AttributesTest\Attributes;

#[\Attribute]
class NameAttribute
{
    protected string $name;

    /**
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}